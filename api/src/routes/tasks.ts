// ─── Serialise DB row ─────────────────────────────────────────────────────────

function serialize(task: typeof tasks.$inferSelect) {
  return {
    id:          task.id,
    onchainId:   task.onchainId,
    requester:   task.requester,
    worker:      task.worker,
    title:       task.title,
    description: task.description,
    amountWei:   task.amountWei?.toString(),
    status:      task.status,
    result:      task.resultContent,
    resultHash:  task.resultHash,
    deadlineAt:  task.deadlineAt.toISOString(),
    claimedAt:   task.claimedAt?.toISOString() ?? null,
    createdAt:   task.createdAt.toISOString(),
    updatedAt:   task.updatedAt.toISOString(),
    // Encrypted fields
    encryptedPayload:          task.encryptedPayload,
    keyWrapForRequester:       task.keyWrapForRequester,
    keyWrapForWorker:          task.keyWrapForWorker,
    encryptedResult:           task.encryptedResult,
    resultKeyWrapForRequester: task.resultKeyWrapForRequester,
  };
}

export default router;
